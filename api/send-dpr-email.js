export default async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*')
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS')
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type')

  if (req.method === 'OPTIONS') {
    return res.status(200).end()
  }

  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method not allowed' })
  }

  const RESEND_API_KEY = process.env.RESEND_API_KEY

  if (!RESEND_API_KEY) {
    return res.status(500).json({ error: 'RESEND_API_KEY not configured' })
  }

  try {
    const { to, subject, projectName, reportDate, pdfBase64, pdfFilename } = req.body

    if (!to?.length || !pdfBase64) {
      return res.status(400).json({ error: 'Missing required fields: to, pdfBase64' })
    }

    const response = await fetch('https://api.resend.com/emails', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${RESEND_API_KEY}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        from: 'Pipe-Up DPR <noreply@pipe-up.ca>',
        to: Array.isArray(to) ? to : [to],
        subject: subject || `Daily Progress Report - ${reportDate}`,
        html: `
          <div style="font-family: Arial, sans-serif; max-width: 600px; margin: 0 auto;">
            <h2 style="color: #333; margin-bottom: 4px;">Daily Progress Report</h2>
            <p style="color: #666; margin-top: 0;">${projectName || 'Pipeline Project'} — ${reportDate}</p>
            <p style="color: #444;">Please find attached the Daily Progress Report for ${reportDate}.</p>
            <hr style="border: none; border-top: 1px solid #ddd; margin: 20px 0;" />
            <p style="color: #999; font-size: 12px;">
              Generated by Pipe-Up Pipeline Inspector Platform<br/>
              <a href="https://app.pipe-up.ca" style="color: #1976d2;">app.pipe-up.ca</a>
            </p>
          </div>
        `,
        attachments: [
          {
            filename: pdfFilename || 'DPR.pdf',
            content: pdfBase64,
          },
        ],
      }),
    })

    const result = await response.json()

    if (!response.ok) {
      console.error('Resend DPR email error:', result)
      return res.status(500).json({ error: result?.message || 'Failed to send email' })
    }

    return res.status(200).json({ success: true, emailId: result?.id })
  } catch (err) {
    console.error('DPR email error:', err)
    return res.status(500).json({ error: err.message })
  }
}
